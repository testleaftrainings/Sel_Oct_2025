package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
		/*
		 * int x=1; System.out.println(x++); System.out.println("1");
		 * System.out.println("2");
		 */
		/*
		 * for loop:
		 * syntax:
		 * initialization  condition iteration
		 * shortcut:type for and do ctrl+space
		 * 
		 */
		for (int i = 1; i <=5; i++) {
			System.out.println(i);
			
		}
		System.out.println("--------------------");
		for (int i = 5; i >=1; i--) {
			System.out.println(i);
			
		}
		

	}

}
